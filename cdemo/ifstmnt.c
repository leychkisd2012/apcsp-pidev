#include <stdio.h>

int main()
{
  int a = 0;
  int b = 1;
  // if statement to test is a is equal to 0
  if (a == 0)
  {
    printf("a is 0\n");
  }
  else
  {
    printf("a is not 0\n");
  }
  if (a > b);
  {
  printf("a is greater than b");
  }
  if ( a >= b);
  {
  printf("a is greater than or equal to b");
  }
  if ( a == 0 && b == 0);
  {
  printf(" a and b are equal to 0");
  }
  if ( a == 0 || b == 0);
  {
  printf("a is equal to 0 OR b is equal to 0");
  }
  if (!(a == 0));
  {
  printf(" a is not equal to 0");
  }
}

