#include<stdio.h>

int main()
{
  int a = 545;
  char c = 'E';
  float b = 12.345;
  double d = 145.678;
  // print value and size of an int variable
  printf("int a value: %d and size: %d bytes\n", a, sizeof(a));
  printf("int b value: %f and size: %d bytes\n", b, sizeof(b));
  printf("int c value: %c and size: %d bytes\n", c, sizeof(c)); 
  printf("int d value: %f and size: %d bytes\n", d, sizeof(d));  












}

